fn generate_shader_section(entries: &[toml::Value]) -> String {
    let mut constants = String::new();
    let mut manifest_entries = String::new();

    for (index, entry) in entries.iter().enumerate() {
        let name = required_str(entry, "name", "shader");
        let vert = required_str(entry, "vert", "shader");
        let frag = required_str(entry, "frag", "shader");
        let const_name = name.to_ascii_uppercase();

        constants.push_str(&format!(
            "pub const {const_name}: ShaderUid = ShaderUid::new({index});\n"
        ));
        manifest_entries.push_str(&format!(
            "ShaderManifestEntry {{ uid: shader::{const_name}, vert: \"{vert}\", frag: \"{frag}\" }},\n"
        ));
    }

    format!(
        r#"pub mod shader {{
    use crate::resources::uid::ShaderUid;

{constants}}}

pub const SHADERS: &[ShaderManifestEntry] = &[
{manifest_entries}];"#
    )
}

fn required_str<'a>(entry: &'a toml::Value, key: &str, kind: &str) -> &'a str {
    entry
        .get(key)
        .and_then(|value| value.as_str())
        .unwrap_or_else(|| panic!("{kind} entry requires {key}"))
}
